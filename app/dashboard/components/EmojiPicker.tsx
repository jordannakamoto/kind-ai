export default function EmojiPicker({ selectedEmoji, onEmojiSelect, onClose }: EmojiPickerProps) {
  const [searchTerm, setSearchTerm] = useState('');
  const [activeCategory, setActiveCategory] = useState('Smileys & Emotion');

  const filteredEmojis = searchTerm
    ? Object.values(emojiCategories).flat().filter(emoji =>
        emoji.includes(searchTerm.toLowerCase())
      )
    : emojiCategories[activeCategory as keyof typeof emojiCategories] || [];

  return (
    <div className="fixed inset-0 bg-black/50 flex items-center justify-center z-[60]">
      <div className="bg-white rounded-lg shadow-xl w-80 mx-4 max-h-[70vh] overflow-hidden">
        {/* Header */}
        <div className="flex items-center justify-between p-3 border-b">
          <h3 className="text-sm font-semibold">Pick Emoji</h3>
          <button onClick={onClose} className="p-1 hover:bg-gray-100 rounded">
            <X className="w-4 h-4" />
          </button>
        </div>

        {/* Search */}
        <div className="p-3 border-b">
          <div className="relative">
            <Search className="absolute left-2 top-1/2 transform -translate-y-1/2 w-3 h-3 text-gray-400" />
            <input
              type="text"
              placeholder="Search..."
              value={searchTerm}
              onChange={(e) => setSearchTerm(e.target.value)}
              className="w-full pl-7 pr-2 py-1.5 text-sm border border-gray-300 rounded focus:ring-1 focus:ring-blue-500"
            />
          </div>
        </div>

        {/* Categories */}
        {!searchTerm && (
          <div className="flex overflow-x-auto border-b bg-gray-50 text-xs">
            {Object.keys(emojiCategories).map((category) => {
              const shortName = category.split(' ')[0]; // Just first word
              return (
                <button
                  key={category}
                  onClick={() => setActiveCategory(category)}
                  className={`px-2 py-1.5 font-medium whitespace-nowrap transition-colors relative ${
                    activeCategory === category
                      ? 'text-blue-600 bg-white'
                      : 'text-gray-600 hover:text-gray-800'
                  }`}
                >
                  {shortName}
                  {activeCategory === category && (
                    <div className="absolute bottom-0 left-1/2 transform -translate-x-1/2 w-4 h-0.5 bg-blue-600 rounded-full"></div>
                  )}
                </button>
              );
            })}
          </div>
        )}

        {/* Emoji Grid */}
        <div className="p-3 overflow-y-auto" style={{ maxHeight: '300px' }}>
          <div className="grid grid-cols-10 gap-1">
            {filteredEmojis.map((emoji, index) => (
              <button
                key={`${emoji}-${index}`}
                onClick={() => {
                  onEmojiSelect(emoji);
                  onClose();
                }}
                className={`p-1.5 text-lg hover:bg-gray-100 rounded transition-colors ${
                  selectedEmoji === emoji ? 'bg-blue-100 ring-1 ring-blue-500' : ''
                }`}
                title={emoji}
              >
                {emoji}
              </button>
            ))}
          </div>
          {filteredEmojis.length === 0 && searchTerm && (
            <p className="text-center text-gray-500 py-6 text-sm">No emojis found</p>
          )}
        </div>

      </div>
    </div>
  );
}